package com.sparta.aa.basics;

public class Loops {
    public static void main(String[] args) {
        double[] doubleArr = {2.1, 2.3};

        for (double item : doubleArr) {
           System.out.println(item);
        }

        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

    }
}
